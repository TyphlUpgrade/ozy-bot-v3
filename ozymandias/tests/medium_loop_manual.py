"""
tests/medium_loop_manual.py
============================
One-shot manual exercise of _medium_loop_cycle() with controlled mock data.

Scenario
--------
- Watchlist: 2 Tier 1 symbols (AAPL, TSLA) + 1 Tier 2 (NVDA — should NOT be scanned)
- Portfolio: 1 open position (MSFT — included in scan even though not on watchlist)
  So scan list = [AAPL, TSLA, MSFT]
- YFinanceAdapter.fetch_bars mocked to return real-shaped 100-row DataFrames for
  each of those 3 symbols.
- ReasoningCache seeded with a plausible Claude result (as if slow loop ran 5 min ago)
  containing 2 opportunities: AAPL (conviction 0.78) and TSLA (conviction 0.65).
- AlpacaBroker.get_account and place_order mocked.

Expected behaviour
------------------
A. TA computed for all 3 symbols — composite scores present, no NaN.
B. Entry signals generated by strategies for at least the symbols with
   favourable indicators (depends on synthetic data shape).
C. Ranker sees Claude opportunities + fresh TA, produces a scored list.
D. The parsed_response is read correctly (not the top-level cache envelope)
   — if the raw cache record is passed instead of parsed_response, opportunities
   will be empty and this assertion will fail.
E. Top opportunity (if any passes filters) triggers fill_protection gate check
   and, if clear, attempts place_order.
F. evaluate_position() runs on the MSFT position without error.

Run with:
    PYTHONPATH=. python ozymandias/tests/medium_loop_manual.py
"""
