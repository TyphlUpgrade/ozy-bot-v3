def _build_mock_adapter(bars: dict[str, pd.DataFrame]):
    """Adapter that serves pre-built DataFrames instead of calling yfinance."""
    adapter = MagicMock()
    async def fetch_bars(symbol, interval, period):
        df = bars.get(symbol)
        if df is None:
            raise ValueError(f"No mock bars for {symbol}")
        return df
    adapter.fetch_bars = fetch_bars
    return adapter


def _build_mock_broker(order_id: str = "test-ord-001"):
    from ozymandias.execution.broker_interface import AccountInfo, OrderResult
    account = AccountInfo(
        equity=100_000.0, buying_power=80_000.0, cash=50_000.0,
        currency="USD", pdt_flag=False, daytrade_count=0, account_id="mock-001",
    )
    result = OrderResult(
        order_id=order_id,
        status="new",
        submitted_at=datetime.now(timezone.utc),
    )
    broker = MagicMock()
    broker.get_account  = AsyncMock(return_value=account)
    broker.place_order  = AsyncMock(return_value=result)
    return broker


