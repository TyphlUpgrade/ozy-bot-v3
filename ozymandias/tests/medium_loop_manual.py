async def _seed_state(orch) -> None:
    from ozymandias.core.state_manager import (
        ExitTargets, PortfolioState, Position,
        TradeIntention, WatchlistEntry, WatchlistState,
    )
    now_iso = datetime.now(timezone.utc).isoformat()

    watchlist = WatchlistState(entries=[
        WatchlistEntry(symbol="AAPL", date_added=now_iso,
                       reason="strong momentum", priority_tier=1),
        WatchlistEntry(symbol="TSLA", date_added=now_iso,
                       reason="volatility play", priority_tier=1),
        WatchlistEntry(symbol="NVDA", date_added=now_iso,
                       reason="monitoring", priority_tier=2),   # tier 2 — NOT scanned
    ])
    await orch._state_manager.save_watchlist(watchlist)

    portfolio = PortfolioState(
        cash=50_000.0,
        buying_power=80_000.0,
        positions=[
            Position(
                symbol="MSFT",
                shares=30,
                avg_cost=420.00,
                entry_date="2026-03-14",
                intention=TradeIntention(
                    strategy="momentum",
                    exit_targets=ExitTargets(profit_target=450.00, stop_loss=400.00),
                ),
            )
        ],
    )
    await orch._state_manager.save_portfolio(portfolio)


# ---------------------------------------------------------------------------
# Interceptors to observe internal state mid-cycle
# ---------------------------------------------------------------------------

_ta_results:      dict[str, dict] = {}
_entry_signals:   dict[str, list] = {}
_ranked_opps:     list            = []
_place_order_args: list           = []


def _install_interceptors(orch):
    """Monkey-patch key internal calls to capture their outputs for assertions."""
    from ozymandias.intelligence import technical_analysis as ta_mod
    from ozymandias.intelligence import opportunity_ranker as ranker_mod

    orig_summary = ta_mod.generate_signal_summary

    def traced_summary(symbol, df):
        result = orig_summary(symbol, df)
        _ta_results[symbol] = result
        return result

    ta_mod.generate_signal_summary = traced_summary
    # Patch the reference used inside orchestrator
    import ozymandias.core.orchestrator as orch_mod
    orch_mod.generate_signal_summary = traced_summary

    orig_rank = orch._ranker.rank_opportunities
    def traced_rank(*args, **kwargs):
        result = orig_rank(*args, **kwargs)
        _ranked_opps.clear()
        _ranked_opps.extend(result)
        return result
    orch._ranker.rank_opportunities = traced_rank


