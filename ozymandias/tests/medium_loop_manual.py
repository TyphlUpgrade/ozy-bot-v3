def _make_bars(symbol: str, n: int = 100, base_price: float = 200.0) -> pd.DataFrame:
    """
    Build a realistic-shaped 5-minute OHLCV DataFrame with:
    - Enough rows (100) for EMA-26, MACD, ATR-14, RSI-14 to initialise
    - Gentle uptrend + noise so indicators produce non-NaN values
    - Volume with realistic intraday variation
    - DatetimeIndex in UTC, 5-minute bars ending ~now
    """
    rng = np.random.default_rng(seed=abs(hash(symbol)) % (2**32))
    now = datetime.now(timezone.utc).replace(second=0, microsecond=0)
    # Build backwards from now in 5-minute steps
    times = [now - timedelta(minutes=5 * i) for i in range(n, 0, -1)]

    prices = [base_price]
    for _ in range(n - 1):
        prices.append(prices[-1] * (1 + rng.normal(0.0002, 0.003)))

    closes = np.array(prices)
    highs  = closes * (1 + rng.uniform(0.001, 0.006, n))
    lows   = closes * (1 - rng.uniform(0.001, 0.006, n))
    opens  = np.roll(closes, 1)
    opens[0] = closes[0]
    volumes = rng.integers(50_000, 500_000, n).astype(float)

    return pd.DataFrame({
        "open":   opens,
        "high":   highs,
        "low":    lows,
        "close":  closes,
        "volume": volumes,
    }, index=pd.DatetimeIndex(times, tz=timezone.utc))


